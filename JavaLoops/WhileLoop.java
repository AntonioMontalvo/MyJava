public class WhileLoop {

	public static void main(String args[]){
		int x = 0;

		while ( x <= 10 ){
			System.out.print("I count number: " + x );
			x++;// we increase the counter
			System.out.print("\n");
		}
	}
}

// A while loop statement in java repeatedly executes a target statement as long as a given condition is true. When tge condition becomes false, program passes control to the line immediately following the loop. 

// Keep in mind that the loop might not ever run.